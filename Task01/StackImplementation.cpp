#include <iostream>
#include "Stack.h"
#include "Node.h"

/**
 * Добавляет новый элемент на вершину стека.
 *
 * Функция создает новый узел с переданными данными и помещает его на вершину стека.
 * Новый узел становится новой вершиной, а его указатель next связывается
 * с предыдущей вершиной. Операция выполняется за O(1).
 *
 * @param node_data Целочисленное значение, которое будет добавлено в стек
 * @post Стек содержит новый элемент на вершине, размер стека увеличивается на 1
 * @throws std::bad_alloc Если не удается выделить память для нового узла
 */
void Stack::push(int node_data) {
    TNode* newNode = new TNode(node_data); // Создаем новый узел
    newNode->next = top; // Новый узел указывает на старую вершину
    top = newNode; // Новый узел становится вершиной стека
}


/**
 * Отображает все элементы стека на экране в порядке от вершины к основанию.
 *
 * Выполняет последовательный обход стека от вершины к основанию,
 * выводя значения всех элементов через пробел. После вывода всех
 * элементов осуществляется переход на новую строку.
 *
 * @note Метод не изменяет состояние стека
 * @note Если стек пуст, выводится только пустая строка
 */
void Stack::show() const {
    TNode* current = top;
    while (current != nullptr)
    {
        //cout << current->x << " ";
        std::cout << current->value << " ";
        current = current->next;
    }
    //cout << endl;
    std::cout << std::endl;
}

/**
 * Удаляет все элементы стека и освобождает занимаемую ими память.
 *
 * Последовательно удаляет все узлы стека от вершины до основания,
 * освобождая память, выделенную под каждый узел. После завершения
 * работы метода стек становится пустым (top = nullptr).
 *
 * @param stack_to_delete Ссылка на стек, который необходимо удалить
 * @post Переданный стек становится пустым, вся выделенная память освобождена
 */
void Stack::delete_data(Stack& stack_to_delete) {
    if (stack_to_delete.is_empty()) {
        std::cout << "Стек пуст! " << std::endl;
        return;
    }
    
    while (!stack_to_delete.is_empty()) {
        //Node* current = top;
        TNode* current = stack_to_delete.top;
        //top = top->next;
        stack_to_delete.top = stack_to_delete.top->next;
        delete current;
    }

}

/**
 * Отображает адрес вершины стека на экране.
 *
 * Выводит значение указателя на вершину стека (адрес в памяти),
 * что может быть полезно для отладки и демонстрационных целей.
 *
 * @note Если стек пуст, будет выведен адрес nullptr (обычно 0)
 */
void Stack::show_address() const {
    //cout << "Адрес вершины стека: " << top << endl;
    std::cout << "Адрес вершины стека: " << top << std::endl;
}

/**
 * Возвращает значение элемента на вершине стека без его удаления.
 *
 * Предоставляет доступ к данным верхнего элемента стека для чтения.
 *
 * @return Целочисленное значение, хранящееся на вершине стека.
 *          Если стек пуст, возвращает INT_MIN (минимально возможное значение int)
 *          как индикатор ошибки.
 * @warning FIX_ME: Возврат 1 при пустом стеке может ввести в заблуждение,
 *          так как 1 может быть корректным значением данных.
 *          Рекомендуется либо выбрасывать исключение, либо использовать
 *          std::optional, либо изменить логику проверки на стороне вызова.
 * @see is_empty() для проверки пустоты стека перед вызовом
 */
int Stack::get_top_value() const
{
    if (top != nullptr)
    {
        //return top->x;
        return top->value;
    }
    // FIX_ME: Не рекомендовано использовать "волшебное" значение.
    //return 1;
    return INT_MIN;
}


/**
 * Проверяет, пуст ли стек.
 *
 * Позволяет определить, содержит ли стек какие-либо элементы.
 * Используется для предотвращения операций с пустым стеком.
 *
 * @return true - если стек пуст (top == nullptr), false - в противном случае
 */
bool Stack::is_empty() const
{
    return top == nullptr;
}